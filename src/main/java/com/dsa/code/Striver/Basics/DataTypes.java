package com.dsa.code.Striver.Basics;

public class DataTypes {
    public static void main(String[] arg){

        int integer = 100 ;
        float decimal = 70.21f;
        double doubleNumber = 100d;
        byte byte_data = 100;
        long long_data = 19000000;
        char c ='M';
        boolean flag = true;
        short shortVal = 300;

        System.out.println("Integer: "+ integer);
        System.out.println("Decimal: "+decimal);
        System.out.println("Double:"+doubleNumber);
        System.out.println("Byte :"+byte_data);
        System.out.println("Character : "+c);
        System.out.println("Long : "+long_data);
        System.out.println("Flag : "+flag);
        System.out.println("Short : "+shortVal);


/*
        byte	Stores  numbers from -128 to 127 range
        short	Stores  numbers from -32,768 to 32,767 range
        int	Stores numbers from -2,147,483,648 to 2,147,483,647
        long	Stores  numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        float	Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
        double	Stores fractional numbers. Sufficient for storing 15 to 16 decimal digits
        boolean	Stores true or false values
        char	Stores a single character/letter or ASCII values

 */





    }
}
